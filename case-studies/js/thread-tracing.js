  function applyLang(lang) {
    const dict = I18N[lang] || {};
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (lang === 'en') {
        const orig = el.getAttribute('data-i18n-orig');
        if (orig != null) el.innerHTML = orig;
      } else if (dict[key] != null) {
        if (!el.hasAttribute('data-i18n-orig')) el.setAttribute('data-i18n-orig', el.innerHTML);
        el.innerHTML = dict[key];
      }
    });
    const htmlEl = document.documentElement;
    if (lang === 'en') htmlEl.setAttribute('lang', 'en');
    else if (lang === 'ja') htmlEl.setAttribute('lang', 'ja');
    else if (lang === 'zh-tw') htmlEl.setAttribute('lang', 'zh-TW');
    else if (lang === 'zh-cn') htmlEl.setAttribute('lang', 'zh-CN');
  }

  // -- Loop return bar alignment --
  function alignLoopBar() {
    const wrap = document.querySelector('.loop-wrap');
    if (!wrap) return;
    const nodes = wrap.querySelectorAll('.loop-node');
    const bar = wrap.querySelector('.loop-return-bar');
    if (nodes.length < 2 || !bar) return;
    const wrapRect = wrap.getBoundingClientRect();
    const firstRect = nodes[0].getBoundingClientRect();
    const lastRect = nodes[nodes.length - 1].getBoundingClientRect();
    const left = firstRect.left - wrapRect.left + firstRect.width / 2;
    const right = wrapRect.width - (lastRect.left - wrapRect.left + lastRect.width / 2);
    bar.style.marginLeft = left + 'px';
    bar.style.marginRight = right + 'px';
  }
  window.addEventListener('load', alignLoopBar);
  window.addEventListener('resize', alignLoopBar);

  // -- Language switcher --
  (function() {
    const sel = document.getElementById('nav-lang-select');
    if (!sel) return;
    try {
      const saved = localStorage.getItem('portfolio-lang');
      if (saved) { sel.value = saved; applyLang(saved); }
    } catch(e) {}
    sel.addEventListener('change', () => {
      try { localStorage.setItem('portfolio-lang', sel.value); } catch(e) {}
      applyLang(sel.value);
      requestAnimationFrame(alignLoopBar);
    });
  })();
