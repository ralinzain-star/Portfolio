  (() => {
    const wrap = document.querySelector('.cjm-wrap');
    if (!wrap) return;

    const dots = wrap.querySelectorAll('.cjm-dot');
    const hits = wrap.querySelectorAll('.cjm-dot-hit');
    const bubbles = wrap.querySelectorAll('.cjm-bubble');

    // Dot appear delays: timed after each dot's CSS animation
    const dotDelays = [500, 700, 900, 1100, 1300, 1500];
    const SHOW_DUR = 900; // how long each bubble stays visible during auto-sequence
    const hoveredStages = new Set();

    function showBubblesForStage(stage) {
      bubbles.forEach(b => { if (b.dataset.stage === String(stage)) b.classList.add('visible'); });
    }
    function hideBubblesForStage(stage) {
      if (hoveredStages.has(String(stage))) return; // don't hide if currently hovered
      bubbles.forEach(b => { if (b.dataset.stage === String(stage)) b.classList.remove('visible'); });
    }

    const isMobile = () => window.innerWidth <= 640;

    // Mobile: show bubbles only for the dot closest to the centre of the scroll container
    function updateMobileBubbles() {
      const outer = wrap.closest('.cjm-outer');
      if (!outer) return;
      const maxScroll = outer.scrollWidth - outer.clientWidth;
      let closestStage;
      if (outer.scrollLeft <= 8) {
        closestStage = '0';
      } else if (outer.scrollLeft >= maxScroll - 8) {
        closestStage = String(dots.length - 1);
      } else {
        const outerRect = outer.getBoundingClientRect();
        const viewCenterX = outerRect.left + outerRect.width / 2;
        let minDist = Infinity;
        dots.forEach(dot => {
          const dotRect = dot.getBoundingClientRect();
          const dotCenterX = dotRect.left + dotRect.width / 2;
          const dist = Math.abs(dotCenterX - viewCenterX);
          if (dist < minDist) { minDist = dist; closestStage = dot.dataset.stage; }
        });
      }
      bubbles.forEach(b => {
        if (b.dataset.stage === closestStage) b.classList.add('visible');
        else b.classList.remove('visible');
      });
    }

    // On scroll into view: line + dots animate
    const obs = new IntersectionObserver(entries => {
      if (!entries[0].isIntersecting) return;
      wrap.classList.add('in-view');
      if (isMobile()) {
        // Show bubbles for initially visible stages, then track scroll
        setTimeout(updateMobileBubbles, 500); // wait for dot animation to start
        const outer = wrap.closest('.cjm-outer');
        if (outer) outer.addEventListener('scroll', updateMobileBubbles, { passive: true });
      } else {
        dotDelays.forEach((delay, stage) => {
          setTimeout(() => { showBubblesForStage(stage); }, delay + 300);
          setTimeout(() => { hideBubblesForStage(stage); }, delay + 300 + SHOW_DUR);
        });
      }
      obs.disconnect();
    }, { threshold: 0.15 });
    obs.observe(wrap);

    // Dot hover → show bubble, hide on leave (desktop only)
    if (!isMobile()) {
      hits.forEach(hit => {
        const stage = hit.dataset.stage;
        hit.addEventListener('mouseenter', () => {
          hoveredStages.add(stage);
          showBubblesForStage(stage);
          dots.forEach(d => { if (d.dataset.stage === stage) d.classList.add('active'); });
        });
        hit.addEventListener('mouseleave', () => {
          hoveredStages.delete(stage);
          hideBubblesForStage(stage);
          dots.forEach(d => { if (d.dataset.stage === stage) d.classList.remove('active'); });
        });
      });
    }
  })();

